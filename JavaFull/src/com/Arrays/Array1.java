package com.Arrays;

public class Array1 {
    public static void main(String[] args) {
        int[] num = {2, 3, 4, 5, 6, 9};
        System.out.println(num[3]);

        System.out.println("*************************");

        for (int i = 0; i <= num.length - 1; i++) {
            System.out.println(num[i]);
        }
    }
}
