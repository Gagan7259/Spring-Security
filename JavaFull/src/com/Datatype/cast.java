package com.Datatype;

public class cast {
    public static void main(String[] args) {
        int a = 12;
        byte c = (byte) a;
        System.out.println(c);

        float f = 100.56f;
        int e = (int) f;
        System.out.println(e);

        byte x = 20;
        byte y = 10;
        int result = x * y;
        System.out.println(result);

    }
}
